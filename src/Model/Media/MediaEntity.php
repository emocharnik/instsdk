<?php

namespace InstagramApp\Model\Media;

use InstagramApp\Model\AbstractModel;

/**
 * Class ImageEntity
 * @package InstagramApp\Model\Image
 */
class MediaEntity extends AbstractModel
{
    /** @var int */
    protected $height;

    /** @var int */
    protected $width;

    /** @var string */
    protected $url;

    /**
     * @return int
     */
    public function getHeight(): int
    {
        return $this->height;
    }

    /**
     * @param int $height
     */
    public function setHeight(int $height)
    {
        $this->height = $height;
    }

    /**
     * @return int
     */
    public function getWidth(): int
    {
        return $this->width;
    }

    /**
     * @param int $width
     */
    public function setWidth(int $width)
    {
        $this->width = $width;
    }

    /**
     * @return string
     */
    public function getUrl(): string
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl(string $url)
    {
        $this->url = $url;
    }
}
